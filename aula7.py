# Ex: Cálculo do IMC (Índice de Massa Corporea) + Ellipsis
# ... Ellipsi 
nome = 'Lucas Daniel' #str
altura = 1.80 #float
peso = 95
imc = (peso / (altura * altura)) # IMC = peso /(altura *altura)

# 1° forma de fazer.

print('nome', nome)
print('altura', altura)
print('peso', peso)
print('imc', imc)

# 2° Forma de fazer.

print(nome, 'tem', altura, 'altura')
print('pesa', peso, 'quilos em seu IMC é',)
print(imc)